def _md(*lines: str) -> dict:
    """构造一个 markdown cell。"""
    return {
        "cell_type": "markdown",
        "metadata": {},
        "source": list(lines),
    }


def _code(*lines: str) -> dict:
    """构造一个 code cell,outputs/execution_count 留空。"""
    return {
        "cell_type": "code",
        "execution_count": None,
        "metadata": {},
        "outputs": [],
        "source": list(lines),
    }


def _nb(cells: list[dict]) -> dict:
    """构造一个 .ipynb 文件结构。"""
    return {
        "cells": cells,
        "metadata": {
            "kernelspec": {
                "display_name": "Python 3",
                "language": "python",
                "name": "python3",
            },
            "language_info": {"name": "python", "version": "3.11"},
        },
        "nbformat": NBFORMAT,
        "nbformat_minor": NBFORMAT_MINOR,
    }


